// Final Keyword

// Final Keyword can be used in 3 ways :-

/* 1) Final Variable 
 * 2) Final Method
 * 3) Final class
 */

/* If a variable is declared as final then it will be treated as a constant rather than a normal variable , it means that it's value will remain unchanged 
 * throughout the program after initiliation. A final variable can be initialied only once in a program either on the declaration statement or 
 * inside the constructor.
 */

 /* NOTE :- If a variable is declared as static and final together then it can be initialied either on the declaration statement or inside the static block only */

 /* If a method is declared as final then it cannot be overridden by the child class and if a class is declared as final then it cannot be inherited */
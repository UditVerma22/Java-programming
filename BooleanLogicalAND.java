// Boolean
// Logical Operators
// Logical AND (&&)

class BooleanLogicalAND {
    public static void main(String args[]) {
        int a = 10, b = 10, c = 10;
        boolean x;
        x = a == b && b == c; // Output 10 10 10 True
        System.out.println(a + " " + b + " " + c + " " + x);
    }
}
// IF ELSE
// WAP to check that the given character is vowel or consonant between a to z

public class CheckgivenCharacterisVOWELorCONSONANTusingIFELSE {
    public static void main(String args[]) {
        char ch = 'a';
        if (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u') {
            System.out.println("VOWEL");
        } else {
            System.out.println("CONSONANT");
        }
    }

}

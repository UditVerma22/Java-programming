class Bitwiseleftshift3 {
    public static void main(String[] args) {
        int a = 13 << 3; // Output 104
        int b = 15 << 3; // Output 120
        int c = 19 << 3; // Output 152
        int d = 72 << 3; // Output 576
        System.out.println(a + " " + b + " " + c + " " + d);
    }
}
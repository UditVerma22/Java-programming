// Bitwise Right Shift Operator

class BitwiseRightshift {
    public static void main(String args[]) {
        int a = 10 >> 2; // Output 2
        System.out.println(a);
    }
}

// 0000 0000 0000 0000 0000 0000 0000 1010 (Binary of 10)
// 00 0000 0000 0000 0000 0000 0000 0000 10 (After Right by 2)
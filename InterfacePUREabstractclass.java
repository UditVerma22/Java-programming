// Interface or Pure abstract class

// An interface is a pure abstract form of a class it means that all the method
// declared in an interface are by default public and abstract.

// All the variables declared is an interface are by default public , static and
// final.

/*
 * The concept of interfaces is used to achieve or implement the property of
 * multiple inheritance. A class can implement
 * (inherit) multiple interfaces at the same time.
 * 
 * We can perform following operations -
 * 
 * 1) class extends class
 * 
 * 2) class implements interface 1, interface 2, interface 3, .......
 * 
 * 3) class extends class implements interface 1, interface 2, .......
 * 
 * 4) interface extends interface 1, interface 2 ..........
 */
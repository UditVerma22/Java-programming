/* WAP to declare variable sum */
class sum {
    public static void main(String args[]) {
        int a = 10, b = 20;
        System.out.println(a + b);
        System.out.println(a + " " + b);
        System.out.println(a + "Hello" + b);
        System.out.println("Hello" + a + b);
        System.out.println("Hello" + (a + b));

    }

}

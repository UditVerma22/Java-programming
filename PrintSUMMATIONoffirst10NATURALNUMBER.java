// WAP to print Summation of first 10 Natural Numbers

public class PrintSUMMATIONoffirst10NATURALNUMBER {
    public static void main(String args[]) {
        int i = 1, sum = 0;
        while (i <= 10) {
            sum = sum + i;
            i++;
        }
        System.out.println(sum); // Output 55
    }

}

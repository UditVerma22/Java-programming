// Switch Case

// WAP to check that the given character is vowel or consonant between a to z

class CheckgivenCharacterisVOWELorCONSONANTusingSWITCHCASE {
    public static void main(String args[]) {
        char ch = 'j';
        switch (ch) {
            case 'a':
                System.out.println("VOWEL");
            case 'e':
                System.out.println("VOWEL");
            case 'i':
                System.out.println("VOWEL");
            case 'o':
                System.out.println("VOWEL");
            case 'u':
                System.out.println("VOWEL");
            default:
                System.out.println("CONSONANTS");
        }
    }

}

// Constructor

/*
 * Constructors are special member methods of a class, these are special because
 * :-
 * 1) The name of constructir will always be same as the class name.
 * 
 * 2) It does not have any return type ( not even void ) and we are not allow to
 * return anything from a constructor.
 * 
 * 3) there are three types of contructors -
 * a) Default Constructor (No Argument Constructor)
 * b) Parameterized Contructor
 * c) Copy Constructor
 * 
 * 4) If we does not have any constructor in our class then compiler will
 * generate a default constructor itself.
 * 
 * 5) To create an object it is require to call a constructor first.
 * 
 * 6) We cannot inherit Constructor.
 * 
 * 7) The "new" operator is require to call a constructor.
 */